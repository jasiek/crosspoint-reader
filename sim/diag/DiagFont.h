inline const Glyph* glyphFor(char c) {
  unsigned char uc = static_cast<unsigned char>(c);
  if (uc >= 'a' && uc <= 'z') uc -= 32;  // fold to upper
  if (uc < 32 || uc > 95) return nullptr;
  return &kGlyphs[uc - 32];
}

inline void drawText(SDL_Renderer* r, int x, int y, const char* text,
                     uint8_t cr, uint8_t cg, uint8_t cb) {
  SDL_SetRenderDrawColor(r, cr, cg, cb, 255);
  for (const char* p = text; *p; ++p) {
    const Glyph* g = glyphFor(*p);
    if (g) {
      for (int gy = 0; gy < kGlyphH; ++gy) {
        const char* row = g->row[gy];
        for (int gx = 0; gx < kGlyphW; ++gx) {
          if (row[gx] == '#') SDL_RenderDrawPoint(r, x + gx, y + gy);
        }
      }
    } else {
      // Unknown char: draw a 5x7 outline so it's visible.
      SDL_Rect rect{x, y, kGlyphW, kGlyphH};
      SDL_RenderDrawRect(r, &rect);
    }
    x += kGlyphAdv;
  }
}

